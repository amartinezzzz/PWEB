var n1 = parseFloat(prompt("Digite um número: "));
var n2 = parseFloat(prompt("Digite mais um número: "));

var soma = n1 + n2;
var sub = n1 - n2;
var mult = n1 * n2;
var div = n1 / n2;
var resto = n1 % n2;

alert("A soma dos dois é igual a: " + soma);
alert("A subtração do primeiro menos o segundo é igual a: " + sub);
alert("A multiplicação é igual a: " + mult);
alert("A divisão é igual a: " + div);
alert("O resto é igual a: " + resto);
